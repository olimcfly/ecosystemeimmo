        <p><strong>Tous les vendeurs ne sont pas pareils.</strong> Un sénior qui vend la maison de famille n'a pas les mêmes peurs qu'un investisseur. Une famille qui achète sa première maison ne pose pas les mêmes questions qu'un promoteur.</p>

